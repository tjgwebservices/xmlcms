package com.tjgwebservices.tjgxmlcms.form.calendar;

public class EventAdvertisementForm {

    private Integer id;
    private String title;
    private String subTitle;
    private String adImagePath;
    private String contactInfo;
    private Integer eventId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubTitle() {
        return subTitle;
    }

    public void setSubTitle(String subTitle) {
        this.subTitle = subTitle;
    }

    public String getAdImagePath() {
        return adImagePath;
    }

    public void setAdImagePath(String adImagePath) {
        this.adImagePath = adImagePath;
    }

    public String getContactInfo() {
        return contactInfo;
    }

    public void setContactInfo(String contactInfo) {
        this.contactInfo = contactInfo;
    }

    public Integer getEventId() {
        return eventId;
    }

    public void setEventId(Integer eventId) {
        this.eventId = eventId;
    }

    
}
